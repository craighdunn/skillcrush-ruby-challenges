weather = "rainy"

case(weather)
	when "sunny" 
		puts "Put on some shorts and a t-shirt"
	when "snowy" 
		puts "Call in to work. This is Texas, we don't know how to deal with this"
	when "rainy" 
		puts "Grab your umbrella."
	when "tornado" 
		puts "Get in the bath tub and pray to what ever higher power you belive in."
	when "hellfire" 
		puts "What did you do this time"
	else 
		puts "Check back later. THis is Texas, so the weather will be different in 10 minutes."
end